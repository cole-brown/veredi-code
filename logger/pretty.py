import pprint


# -----------------------------------------------------------------------------
# Constants
# -----------------------------------------------------------------------------


# ------------------------------------------------------------------------------
# Logging / Output Help
# ------------------------------------------------------------------------------

def to_str(data):
    '''
    Tries to pretty print data. Returns a string.
    '''
    return pprint.pformat(data)
